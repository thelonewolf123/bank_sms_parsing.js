export function tokenize(text: string): string[] {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, " ")
    .split(/\s+/)
    .filter((token) => token.length > 1 && !STOPWORDS.has(token));
}

export function normalizeText(text: string): string {
  return text
    .toLowerCase()
    .replace(/\d+/g, "NUM")
    .replace(/xx+\d*/gi, "MASKED")
    .replace(/[^a-z\s]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}
